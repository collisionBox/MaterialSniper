#include "DxLib.h"
#include "environment.h"

Aim::Aim()
{
	// 画像を読み込む
	handle = LoadGraph("img/test.png");
	bgHandle = handle;
	scorpHandle = LoadGraph("img/crosshair.png");
	lectilHandle = LoadGraph("img/lectil.png");
	Init();
}
Aim::~Aim()
{
	DeleteGraph(handle);
	DeleteGraph(lectilHandle);
	DeleteGraph(scorpHandle);
}
void Aim::Init()//初期化
{
	isRightClick = false;
	isLeftClick = false;
	fireFlag = true;
	reloadFlag = false;
	nowTime = 0;
	magazin = maxMagazin + 1;
	carriedNum = carriedNumMax - maxMagazin;
	x = windowX / 2;
	y = windowY / 2;
	aimming = lectilHandle;
}

void Aim::Update(Target& tag, Bullet& bullet, float& gameTime, float& deltaTime)
{

	
	MouseBehavior(tag, gameTime, deltaTime);
	Draw(tag,bullet);
	
}

void Aim::Draw(Target& tag, Bullet& bullet)
{
	//デバッグ用に背景を薄くする
	SetDrawBlendMode(DX_BLENDMODE_ALPHA, 50);
	DrawRotaGraph2(x, y, x, y, ExRate, 0, handle, false);//背景(マウスを中心に拡大)
	SetDrawBlendMode(DX_BLENDMODE_ALPHA, 0);

	tag.Draw(x, y, ExRate, isRightClick,bullet);//的
	
	DrawRotaGraph(x, y, 1, 0, aimming, true);//クロスヘア

	DrawFormatString(50, 50, green, "%f:%f", x,y);
	DrawCircle(x, y, 5, red, true);
	
}

void Aim::MouseBehavior(Target& tag, float& gameTime, float& deltaTime)
{
	GetMousePoint(&mouseX, &mouseY);
	//画面外にでないようにする
	if (mouseX <= 0)
	{
		mouseX = 0;
	}
	if (mouseX >= windowX)
	{
		mouseX = windowX;
	}
	if (mouseY <= 0)
	{
		mouseY = 0;
	}
	if (mouseY >= windowY)
	{
		mouseY = windowY;
	}
	x = mouseX;
	y = mouseY;
	//右クリック（エイム）処理
	if ((GetMouseInput() & MOUSE_INPUT_RIGHT) != 0)//押されているとき
	{
		//LShift（息止め）処理
		if (CheckHitKey(KEY_INPUT_LSHIFT) != 0)//押されているとき
		{
			omega = 0.008;
		}
		else if (CheckHitKey(KEY_INPUT_LSHIFT) == 0)//押されていないとき
		{
			omega = 0.012f;
		}	
		v += omega;
		x += 15 * cos(v);
		y += 15 * sin(v * 2);
		ExRate = magnificationRate;
		aimming = scorpHandle;
		isRightClick = true;

	}
	else if ((GetMouseInput() & MOUSE_INPUT_RIGHT) == 0)//押されていないとき
	{
		ExRate = 1.0f;
		aimming = lectilHandle;
		isRightClick = false;
	}
	if (fireFlag)//弾を撃ったら
	{
		if (GetMouseInput() & MOUSE_INPUT_LEFT || CheckHitKey(KEY_INPUT_Z))
		{
			magazin -= 1;
			tag.HitTest(mouseX, mouseY, isRightClick, gameTime);
			fireFlag = false;

		}
	}
	if (!fireFlag && !(GetMouseInput() & MOUSE_INPUT_LEFT || CheckHitKey(KEY_INPUT_Z)))
	{
		fireFlag = true;
	}
	
	MagazinDirector(gameTime);
}

void Aim::MagazinDirector(float& gameTime)
{
	if (CheckHitKey(KEY_INPUT_R) && magazin < maxMagazin + 1)
	{
		nowTime = gameTime;
		reloadFlag = true;
	}
	if (reloadFlag)
	{
		if (gameTime - nowTime >= reloadTime)
		{
			int subtrahend = (maxMagazin + 1) - magazin;//携行数から引く数
			carriedNum -=subtrahend;
			if (magazin >= 1)
			{
				magazin = maxMagazin + 1;
			}
			else if(magazin == 0)
			{
				magazin = maxMagazin;
			}
			fireFlag = true;
			reloadFlag = false;
		}
	}
	
	if (magazin <= 0)
	{
		fireFlag = false;
	}
	if (magazin <= 0 && carriedNum <= 0)
	{
		fireFlag = false;
	}
}
